    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        // TODO add your handling code here:
        String valor = null;
        String codest = null;
        String tipcta = null;
        String cuenta = null;
        String titulo = null;
        String  descri = null;
        String tipsal = null;
        String Perten = null;
        String Tipo = null;

        if ("".equals(jTxtCta_Codest.getText())){
            JOptionPane.showMessageDialog(null,"Debe Ingresar el Código del Catálogo");
            jTxtCta_Codest.requestFocus();
            return;
        }

        if ("".equals(jTxtCta_Tipcta.getText())){
            JOptionPane.showMessageDialog(null,"Debe Ingresar el Tipo de Cuenta");
            jTxtCta_Tipcta.requestFocus();
            return;
        }

        if ("".equals(jTxtCta_Cuenta.getText())){
            JOptionPane.showMessageDialog(null,"Debe Ingresar la Cuenta");
            jTxtCta_Cuenta.requestFocus();
            return;
        }

        if ("".equals(jTxtCta_Titulo.getText())){
            JOptionPane.showMessageDialog(null,"Debe Ingresar el Título");
            jTxtCta_Titulo.requestFocus();
            return;
        }

        if ("".equals(jTxtCta_Descri.getText())){
            JOptionPane.showMessageDialog(null,"Debe Ingresar la Descripción");
            jTxtCta_Descri.requestFocus();
            return;
        }

        DefaultTableModel model = (DefaultTableModel) this.jTabPlanCta.getModel();
        String [] filas={jTxtCta_Cuenta.getText(),jTxtCta_Titulo.getText(),jCmbCta_Tipsal.getSelectedItem().toString()};
        model.addRow(filas);

        codest = jTxtCta_Codest.getText();
        tipcta = jTxtCta_Tipcta.getText();
        cuenta = jTxtCta_Cuenta.getText();
        titulo = jTxtCta_Titulo.getText();
        descri = jTxtCta_Descri.getText();
        tipsal = jCmbCta_Tipsal.getSelectedItem().toString();

        jTxtCta_Codest.setText("");
        jTxtCta_Tipcta.setText("");
        jTxtCta_Cuenta.setText("");
        jTxtCta_Titulo.setText("");
        jTxtCta_Descri.setText("");

        SQLQuerys insertar = new SQLQuerys();

        insertar.SqlInsert("INSERT INTO DNPLANCTA SET CTA_CODEST='"+codest+"', "+
            "CTA_TIPCTA='"+tipcta+"',"+
            "CTA_CUENTA='"+cuenta+"',"+
            "CTA_TITULO='"+titulo+"',"+
            "CTA_DESCRI='"+descri+"',"+
            "CTA_TIPSAL='"+tipsal.substring(0, 1)+"', "+
            "CTA_PERTEN='"+Perten+"', "+
            "CTA_TIPO='"+Tipo+"'"
        );
        JOptionPane.showMessageDialog(null, "ssss");
        jTxtCta_Codest.requestFocus();
    }//GEN-LAST:event_jBtnAgregarActionPerformed


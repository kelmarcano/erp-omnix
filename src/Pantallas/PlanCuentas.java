        if (!"".equals(jTxtCta_Codest.getText())){
            cargatabla = new CargaTablas();
            String SQL = "SELECT CTA_CUENTA, CTA_TITULO, IF(CTA_TIPSAL='D','Deudor','Acreedor') AS CTA_TIPSAL FROM DNPLANCTA "+
            "WHERE CTA_CODEST='"+jTxtCta_Codest.getText()+"'  "+
            "ORDER BY CTA_CUENTA";
            String[] columnas = {"Cuenta","Título","Saldo"};

            cargatabla.cargatablas(jTabPlanCta, SQL, columnas);
            jTabPlanCta.getColumn("Cuenta").setWidth(10);   //no esta funcionando ***** DL
        }
